package bl.domain.countries;

public class StatePhilosophy {
    private final int id;
    private final String name;

    public StatePhilosophy(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }


}
